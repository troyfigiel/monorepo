def add_one(number: int) -> int:
    """Add one to your chosen number! It is like magic!

    >>> add_one(5)
    6
    """
    return number + 1


def subtract_one(number: int) -> int:
    """Subtract one from your chosen number. Pretty basic stuff!

    >>> subtract_one(3)
    2
    """
    return number - 1
